    <script>
        function open_modal(url) {
            $.ajax({
                url: url,
                type: "GET",
                data: {},
                dataType: "json",
                cache: false,
                success: function(res) {
                    $("#global-modal-title").html(res.title);
                    $("#global-modal-body").html(res.html);
                    $("#global-modal").modal('show');
                }
            });
        }
        $(document).ready(function() {
            $('#add_products').click(function(e) {
                e.preventDefault();
                var url = "{{ url('add-products') }}";
                open_modal(url);
            });

            $(".product-edit").click(function(e) {
                e.preventDefault();
                var product_id = $(this).data('id');
                open_modal("/edit-products/" + product_id);
            });
        });
